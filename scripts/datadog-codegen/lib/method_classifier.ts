/**
 * Method classifier — determines the swamp method type for each operation
 * and generates the model source code.
 *
 * Classifies operations as:
 * - list: GET returning a collection (with pagination)
 * - get: GET returning a single item
 * - create: POST that creates a resource
 * - update: PUT/PATCH that modifies a resource
 * - delete: DELETE
 * - action: POST that performs an action (not standard CRUD)
 */

import type { GroupedOperation, ServiceGroup } from "./service_grouper.ts";
import { schemaToZod } from "./type_mapper.ts";
import { ZOD_VERSION } from "../config.ts";
import type { PaginationConfig, ServiceConfig } from "../config.ts";

export type MethodType =
  | "list"
  | "get"
  | "create"
  | "update"
  | "delete"
  | "action";

export interface ClassifiedMethod {
  /** swamp method name (e.g., list_monitors, get_monitor) */
  name: string;
  /** The classification */
  type: MethodType;
  /** Description for the method */
  description: string;
  /** The original operation */
  operation: GroupedOperation;
}

/**
 * Classify an operation into a swamp method type.
 */
export function classifyOperation(op: GroupedOperation): MethodType {
  const { httpMethod, isCollection, pagination } = op;

  switch (httpMethod) {
    case "get":
      if (isCollection || pagination.style !== "none") return "list";
      return "get";
    case "post": {
      // If the last path segment is a param placeholder, it's an action on a resource
      const lastSegment = op.path.split("/").pop() ?? "";
      if (lastSegment.startsWith("{")) return "action";
      // POST endpoints with "Delete" in operationId are bulk deletes
      if (op.operationId.toLowerCase().includes("delete")) return "action";
      // Check if it's a search/query action (common in DD)
      if (
        op.operationId.toLowerCase().includes("search") ||
        op.operationId.toLowerCase().includes("aggregate")
      ) {
        return "action";
      }
      if (op.requestBody?.properties) return "create";
      return "action";
    }
    case "put":
    case "patch":
      return "update";
    case "delete":
      return "delete";
    default:
      return "action";
  }
}

/**
 * Generate a method name from the operation.
 */
export function generateMethodName(
  op: GroupedOperation,
  type: MethodType,
): string {
  if (op.operationId) {
    let name = op.operationId;

    // DD operationIds look like "ListMonitors", "GetMonitor", "CreateMonitor"
    // Convert PascalCase/camelCase to snake_case
    name = name.replace(/([a-z])([A-Z])/g, "$1_$2")
      .replace(/([A-Z])([A-Z][a-z])/g, "$1_$2")
      .toLowerCase();

    // Remove version suffixes (e.g., _v2)
    name = name.replace(/_v\d+$/, "");

    return sanitizeMethodName(name);
  }

  // Fallback: construct from path
  const segments = op.path.split("/").filter((s) => s && !s.startsWith("{"));
  const lastSegments = segments.slice(-2);
  const resource = lastSegments.join("_");
  return sanitizeMethodName(`${type}_${resource}`);
}

function sanitizeMethodName(name: string): string {
  return name
    .replace(/[^a-zA-Z0-9_]/g, "_")
    .replace(/_+/g, "_")
    .replace(/^_|_$/g, "")
    .toLowerCase();
}

/**
 * Classify all operations in a service group into methods.
 * De-duplicates by method name (first wins).
 */
export function classifyServiceMethods(
  group: ServiceGroup,
): ClassifiedMethod[] {
  const methods: ClassifiedMethod[] = [];
  const seen = new Set<string>();

  for (const op of group.operations) {
    const type = classifyOperation(op);
    const name = generateMethodName(op, type);

    if (seen.has(name)) continue;
    seen.add(name);

    methods.push({
      name,
      type,
      description: op.summary || op.description || `${type} operation`,
      operation: op,
    });
  }

  return methods;
}

/**
 * Generate the complete model TypeScript source file for a service.
 */
export function generateModelSource(
  group: ServiceGroup,
  methods: ClassifiedMethod[],
  version: string,
): string {
  const { config } = group;
  const modelType = `@webframp/datadog/${config.name}`;

  const lines: string[] = [];

  // Header
  lines.push(`/**`);
  lines.push(` * ${config.description}`);
  lines.push(` *`);
  lines.push(
    ` * Auto-generated by scripts/datadog-codegen — do not edit manually.`,
  );
  lines.push(` *`);
  lines.push(` * @module`);
  lines.push(` */`);
  lines.push(
    `// SPDX-License-Identifier: AGPL-3.0-or-later WITH Swamp-Extension-Exception`,
  );
  lines.push(``);
  lines.push(`import { z } from "npm:zod@${ZOD_VERSION}";`);

  // Import API helpers based on what's used
  const usesSingle = methods.some((m) => m.type !== "list");
  const usesPaginated = methods.some((m) => m.type === "list");
  const apiImports: string[] = [];
  if (usesSingle) apiImports.push("ddApi");
  if (usesPaginated) apiImports.push("ddApiPaginated");
  if (apiImports.length > 0) {
    lines.push(
      `import { ${apiImports.join(", ")} } from "./_lib/api.ts";`,
    );
  }
  lines.push(``);

  // Schemas section
  lines.push(
    `// =============================================================================`,
  );
  lines.push(`// Schemas`);
  lines.push(
    `// =============================================================================`,
  );
  lines.push(``);

  // GlobalArgs
  lines.push(generateGlobalArgsSchema());
  lines.push(``);

  // Generate response schemas
  const schemaNames = generateResponseSchemas(methods, lines);

  // Model definition
  lines.push(
    `// =============================================================================`,
  );
  lines.push(`// Model Definition`);
  lines.push(
    `// =============================================================================`,
  );
  lines.push(``);
  lines.push(`/** ${config.description} */`);
  lines.push(`export const model = {`);
  lines.push(`  type: "${modelType}",`);
  lines.push(`  version: "${version}",`);
  lines.push(`  globalArguments: GlobalArgsSchema,`);
  lines.push(``);
  lines.push(`  upgrades: [],`);
  lines.push(``);

  // Resources
  lines.push(`  resources: {`);
  const seenResources = new Set<string>();
  for (const method of methods) {
    if (method.type === "delete") continue;
    const resourceName = getResourceName(method);
    if (seenResources.has(resourceName)) continue;
    seenResources.add(resourceName);
    lines.push(`    "${resourceName}": {`);
    lines.push(
      `      description: "${escapeStr(method.description)}",`,
    );
    lines.push(
      `      schema: ${schemaNames.get(method.name) ?? "z.object({})"},`,
    );
    lines.push(`      lifetime: "infinite" as const,`);
    lines.push(
      `      garbageCollection: ${method.type === "list" ? 10 : 20},`,
    );
    lines.push(`    },`);
  }
  lines.push(`  },`);
  lines.push(``);

  // Methods
  lines.push(`  methods: {`);
  for (const method of methods) {
    lines.push(generateMethod(method, config, schemaNames));
  }
  lines.push(`  },`);
  lines.push(`};`);
  lines.push(``);

  return lines.join("\n");
}

/** Generate GlobalArgsSchema — always apiKey + appKey + site */
function generateGlobalArgsSchema(): string {
  return `const GlobalArgsSchema = z.object({
  apiKey: z.string().meta({ sensitive: true }).describe("Datadog API key (DD-API-KEY)"),
  appKey: z.string().meta({ sensitive: true }).describe("Datadog application key (DD-APPLICATION-KEY)"),
  site: z.enum(["us1", "us3", "us5", "eu1", "ap1", "us1-fed"]).default("us1").describe("Datadog site"),
});`;
}

/** Get the resource name for a method */
function getResourceName(method: ClassifiedMethod): string {
  if (method.type === "list") {
    return method.name.replace(/^(list|get)_/, "");
  }
  return method.name.replace(/^(get|create|update|action|delete)_/, "");
}

/** Generate Zod schemas for all unique response shapes */
function generateResponseSchemas(
  methods: ClassifiedMethod[],
  lines: string[],
): Map<string, string> {
  const schemaNames = new Map<string, string>();

  const seenResources = new Set<string>();
  const methodsWithResources: ClassifiedMethod[] = [];

  for (const method of methods) {
    if (method.type === "delete") continue;
    const resourceName = getResourceName(method);
    if (!seenResources.has(resourceName)) {
      seenResources.add(resourceName);
      methodsWithResources.push(method);
    }
  }

  for (const method of methodsWithResources) {
    const schema = method.operation.responseSchema;
    if (!schema) {
      schemaNames.set(method.name, "z.object({})");
      continue;
    }

    const varName = toPascalCase(method.name) + "Schema";
    schemaNames.set(method.name, varName);

    if (method.type === "list") {
      const itemVarName =
        toPascalCase(method.name.replace(/^(list|get)_/, "")) +
        "ItemSchema";
      const itemZod = schemaToZod(schema, { indent: 2 }, 1);
      lines.push(`const ${itemVarName} = ${itemZod};`);
      lines.push(``);
      lines.push(`const ${varName} = z.object({`);
      lines.push(`  items: z.array(${itemVarName}),`);
      lines.push(`  truncated: z.boolean(),`);
      lines.push(`  fetchedAt: z.string(),`);
      lines.push(`});`);
    } else {
      const zodStr = schemaToZod(schema, { indent: 2 }, 1);
      lines.push(`const ${varName} = ${zodStr};`);
    }
    lines.push(``);
  }

  // Map secondary methods to existing schemas
  for (const method of methods) {
    if (method.type === "delete") continue;
    if (schemaNames.has(method.name)) continue;
    const resourceName = getResourceName(method);
    const primary = methodsWithResources.find((m) => {
      return getResourceName(m) === resourceName;
    });
    if (primary) {
      schemaNames.set(
        method.name,
        schemaNames.get(primary.name) ?? "z.object({})",
      );
    } else {
      schemaNames.set(method.name, "z.object({})");
    }
  }

  return schemaNames;
}

/** Generate a single method definition */
function generateMethod(
  method: ClassifiedMethod,
  _config: ServiceConfig,
  _schemaNames: Map<string, string>,
): string {
  const { operation, type } = method;
  const lines: string[] = [];
  const indent = "    ";

  lines.push(`${indent}${method.name}: {`);
  lines.push(
    `${indent}  description: "${escapeStr(method.description)}",`,
  );

  // Arguments schema
  lines.push(`${indent}  arguments: ${generateArgsSchema(operation)},`);

  // Execute function
  const argsUsed = methodUsesArgs(method);
  const argsParam = argsUsed ? "args" : "_args";
  lines.push(
    `${indent}  execute: async (${argsParam}: Record<string, unknown>, context: { globalArgs: Record<string, string>; writeResource: (spec: string, instance: string, data: unknown) => Promise<{ name: string }>; logger: { info: (msg: string, props: Record<string, unknown>) => void } }) => {`,
  );

  // Destructure globalArgs
  lines.push(
    `${indent}    const { apiKey, appKey, site } = context.globalArgs;`,
  );

  // Build the API path
  const apiPath = buildApiPath(operation.path);

  if (type === "list") {
    lines.push(generateListBody(method, apiPath, indent));
  } else if (type === "get") {
    lines.push(generateGetBody(method, apiPath, indent));
  } else if (type === "create") {
    lines.push(generateCreateBody(method, apiPath, indent));
  } else if (type === "update") {
    lines.push(generateUpdateBody(method, apiPath, indent));
  } else if (type === "delete") {
    lines.push(generateDeleteBody(method, apiPath, indent));
  } else {
    lines.push(generateActionBody(method, apiPath, indent));
  }

  lines.push(`${indent}  },`);
  lines.push(`${indent}},`);

  return lines.join("\n");
}

/** Generate the arguments schema for a method */
function generateArgsSchema(op: GroupedOperation): string {
  const fields: string[] = [];
  const seenFields = new Set<string>();

  // Path params
  for (const p of op.pathParams) {
    const fieldName = sanitizeFieldName(p.name);
    if (seenFields.has(fieldName)) continue;
    seenFields.add(fieldName);
    const desc = p.description
      ? `.describe("${escapeStr(truncateStr(p.description))}")`
      : "";
    fields.push(`  ${fieldName}: z.string()${desc},`);
  }

  // Query params
  for (const p of op.queryParams) {
    const fieldName = sanitizeFieldName(p.name);
    if (seenFields.has(fieldName)) continue;
    seenFields.add(fieldName);
    const desc = p.description
      ? `.describe("${escapeStr(truncateStr(p.description))}")`
      : "";
    let zodType = "z.string()";
    if (p.schema?.type === "integer" || p.schema?.type === "number") {
      zodType = "z.number()";
    } else if (p.schema?.type === "boolean") {
      zodType = "z.boolean()";
    } else if (p.schema?.enum) {
      const vals = (p.schema.enum as string[]).map((v) =>
        `"${escapeStr(String(v))}"`
      );
      zodType = `z.enum([${vals.join(", ")}])`;
    }
    fields.push(`  ${fieldName}: ${zodType}.optional()${desc},`);
  }

  // Request body fields (for create/update)
  if (op.requestBody?.properties) {
    const required = new Set(op.requestBody.required ?? []);
    for (const [name, prop] of Object.entries(op.requestBody.properties)) {
      if (name === "id" || name === "data") continue;
      const fieldName = sanitizeFieldName(name);
      if (seenFields.has(fieldName)) continue;
      seenFields.add(fieldName);
      const fieldZod = schemaToZod(prop, { indent: 2 }, 2);
      const optSuffix = required.has(name) ? "" : ".optional()";
      const desc = prop.description
        ? `.describe("${escapeStr(truncateStr(prop.description))}")`
        : "";
      fields.push(`  ${fieldName}: ${fieldZod}${optSuffix}${desc},`);
    }
  }

  if (fields.length === 0) {
    return "z.object({})";
  }

  return `z.object({\n${fields.join("\n")}\n    })`;
}

/** Check if a method's execute body references args */
function methodUsesArgs(method: ClassifiedMethod): boolean {
  const { type, operation } = method;
  if (type === "list") return true;
  if (operation.pathParams.length > 0) return true;
  if (type === "create" || type === "update") return true;
  if (type === "action" && operation.requestBody) return true;
  if (operation.queryParams.length > 0) return true;
  return false;
}

/** Generate list method body with pagination config */
function generateListBody(
  method: ClassifiedMethod,
  apiPath: string,
  indent: string,
): string {
  const resourceName = method.name.replace(/^(list|get)_/, "");
  const paginationConfig = method.operation.pagination;
  const pathParamNames = method.operation.pathParams
    .map((p) => sanitizeFieldName(p.name));
  const excludeNames = [...pathParamNames];

  return `${indent}    const params: Record<string, string> = {};
${indent}    const excludeKeys = new Set<string>(${
    JSON.stringify(excludeNames)
  });
${indent}    for (const [k, v] of Object.entries(args)) {
${indent}      if (v !== undefined && !excludeKeys.has(k)) params[k] = String(v);
${indent}    }
${indent}
${indent}    const { results, truncated } = await ddApiPaginated(
${indent}      apiKey,
${indent}      appKey,
${indent}      site,
${indent}      \`${apiPath}\`,
${indent}      ${JSON.stringify(paginationConfig)},
${indent}      params,
${indent}    );
${indent}
${indent}    if (truncated) {
${indent}      context.logger.info("WARNING: results truncated at {count} (pagination cap)", { count: results.length });
${indent}    }
${indent}
${indent}    const handle = await context.writeResource("${resourceName}", "main", {
${indent}      items: results,
${indent}      truncated,
${indent}      fetchedAt: new Date().toISOString(),
${indent}    });
${indent}
${indent}    context.logger.info("Found {count} ${resourceName}", { count: results.length });
${indent}    return { dataHandles: [handle] };`;
}

/** Generate get method body */
function generateGetBody(
  method: ClassifiedMethod,
  apiPath: string,
  indent: string,
): string {
  const resourceName = method.name.replace(/^get_/, "");
  const idParam = method.operation.pathParams[
    method.operation.pathParams.length - 1
  ];
  const instanceExpr = idParam
    ? `String(args.${sanitizeFieldName(idParam.name)})`
    : '"latest"';

  const hasQueryParams = method.operation.queryParams.length > 0;
  const pathParamNames = method.operation.pathParams.map((p) =>
    sanitizeFieldName(p.name)
  );

  if (hasQueryParams) {
    return `${indent}    const queryParts: string[] = [];
${indent}    const excludeKeys = new Set<string>(${
      JSON.stringify(pathParamNames)
    });
${indent}    for (const [k, v] of Object.entries(args)) {
${indent}      if (v !== undefined && !excludeKeys.has(k)) queryParts.push(\`\${encodeURIComponent(k)}=\${encodeURIComponent(String(v))}\`);
${indent}    }
${indent}    const qs = queryParts.length > 0 ? \`?\${queryParts.join("&")}\` : "";
${indent}
${indent}    const result = await ddApi(
${indent}      apiKey,
${indent}      appKey,
${indent}      site,
${indent}      "GET",
${indent}      \`${apiPath}\${qs}\`,
${indent}    );
${indent}
${indent}    const handle = await context.writeResource("${resourceName}", ${instanceExpr}, result);
${indent}    context.logger.info("Fetched ${resourceName}", {});
${indent}    return { dataHandles: [handle] };`;
  }

  return `${indent}    const result = await ddApi(
${indent}      apiKey,
${indent}      appKey,
${indent}      site,
${indent}      "GET",
${indent}      \`${apiPath}\`,
${indent}    );
${indent}
${indent}    const handle = await context.writeResource("${resourceName}", ${instanceExpr}, result);
${indent}    context.logger.info("Fetched ${resourceName}", {});
${indent}    return { dataHandles: [handle] };`;
}

/** Generate create method body */
function generateCreateBody(
  method: ClassifiedMethod,
  apiPath: string,
  indent: string,
): string {
  const resourceName = method.name.replace(/^create_/, "");
  const pathParamNames = method.operation.pathParams.map((p) =>
    sanitizeFieldName(p.name)
  );
  const queryParamNames = method.operation.queryParams.map((p) =>
    sanitizeFieldName(p.name)
  );
  const excludeNames = [...pathParamNames, ...queryParamNames];

  return `${indent}    const body: Record<string, unknown> = {};
${indent}    const excludeKeys = new Set<string>(${
    JSON.stringify(excludeNames)
  });
${indent}    for (const [k, v] of Object.entries(args)) {
${indent}      if (!excludeKeys.has(k)) body[k] = v;
${indent}    }
${indent}
${indent}    const result = await ddApi(
${indent}      apiKey,
${indent}      appKey,
${indent}      site,
${indent}      "POST",
${indent}      \`${apiPath}\`,
${indent}      body,
${indent}    );
${indent}
${indent}    const id = (result as { id?: string }).id ?? "created";
${indent}    const handle = await context.writeResource("${resourceName}", id, result);
${indent}    context.logger.info("Created ${resourceName} {id}", { id });
${indent}    return { dataHandles: [handle] };`;
}

