/** Generate the shared _lib/api.ts helper (identical to existing one) */
export function generateApiLib(): string {
  return `// Cloudflare API Helper
// Shared utilities for all Cloudflare models
// Auto-generated by scripts/cloudflare-codegen — do not edit manually.

const CF_API_BASE = "https://api.cloudflare.com/client/v4";

export interface CloudflareResponse<T> {
  success: boolean;
  errors: Array<{ code: number; message: string }>;
  messages: string[];
  result: T;
  result_info?: {
    page: number;
    per_page: number;
    total_count: number;
    total_pages: number;
  };
}

export async function cfApi<T>(
  apiToken: string,
  method: string,
  path: string,
  body?: unknown,
): Promise<T> {
  const url = \`\${CF_API_BASE}\${path}\`;
  const headers: Record<string, string> = {
    "Authorization": \`Bearer \${apiToken}\`,
    "Content-Type": "application/json",
  };

  const response = await fetch(url, {
    method,
    headers,
    body: body ? JSON.stringify(body) : undefined,
  });

  const data = (await response.json()) as CloudflareResponse<T>;

  if (!data.success) {
    const errorMsg = data.errors.map((e) => e.message).join("; ");
    throw new Error(\`Cloudflare API error: \${errorMsg}\`);
  }

  return data.result;
}

const MAX_PAGES = 20;

export interface PaginatedResult<T> {
  results: T[];
  truncated: boolean;
  totalFetched: number;
}

export async function cfApiPaginated<T>(
  apiToken: string,
  path: string,
  params?: Record<string, string>,
): Promise<PaginatedResult<T>> {
  const allResults: T[] = [];
  let page = 1;
  const perPage = 50;
  let truncated = false;

  while (page <= MAX_PAGES) {
    const queryParams = new URLSearchParams({
      page: String(page),
      per_page: String(perPage),
      ...params,
    });

    const url = \`\${CF_API_BASE}\${path}?\${queryParams}\`;
    const response = await fetch(url, {
      headers: {
        "Authorization": \`Bearer \${apiToken}\`,
        "Content-Type": "application/json",
      },
    });

    const data = (await response.json()) as CloudflareResponse<T[]>;

    if (!data.success) {
      const errorMsg = data.errors.map((e) => e.message).join("; ");
      throw new Error(\`Cloudflare API error: \${errorMsg}\`);
    }

    allResults.push(...data.result);

    if (!data.result_info || page >= data.result_info.total_pages) {
      break;
    }
    page++;
  }

  if (page > MAX_PAGES) {
    truncated = true;
  }

  return { results: allResults, truncated, totalFetched: allResults.length };
}
`;
}

