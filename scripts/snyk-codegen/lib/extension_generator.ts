/**
 * Extension generator — produces the complete directory structure for a generated extension.
 *
 * For each service, generates:
 * - manifest.yaml
 * - deno.json
 * - README.md
 * - LICENSE.md
 * - RELEASE_NOTES.md
 * - .swamp.yaml
 * - .gitignore
 * - extensions/models/snyk/{name}.ts
 * - extensions/models/snyk/{name}_test.ts
 * - extensions/models/snyk/_lib/api.ts (shared helper)
 */

import type { ServiceConfig } from "../config.ts";
import { OUTPUT_BASE, SWAMP_TESTING_VERSION } from "../config.ts";

/** Generate manifest.yaml content */
export function generateManifest(
  config: ServiceConfig,
  version: string,
  modelFileName: string,
): string {
  return `manifestVersion: 1
name: "@webframp/snyk/${config.name}"
version: "${version}"
description: "${config.description}"
repository: "https://github.com/webframp/swamp-extensions"
models:
  - snyk/${modelFileName}
platforms:
  - linux-x86_64
  - linux-aarch64
  - darwin-x86_64
  - darwin-aarch64
additionalFiles:
  - README.md
  - LICENSE.md
labels:
${config.labels.map((l) => `  - ${l}`).join("\n")}
`;
}

/** Generate deno.json content */
export function generateDenoJson(): string {
  return JSON.stringify(
    {
      tasks: {
        check:
          "deno check extensions/models/snyk/*.ts extensions/models/snyk/_lib/*.ts",
        lint: "deno lint extensions/models/",
        fmt: "deno fmt extensions/models/",
        "fmt:check": "deno fmt --check extensions/models/",
        test: "deno test --allow-env --allow-net extensions/models/",
      },
      lint: {
        rules: {
          exclude: ["no-import-prefix"],
        },
      },
      imports: {
        "@systeminit/swamp-testing":
          `jsr:@systeminit/swamp-testing@${SWAMP_TESTING_VERSION}`,
      },
    },
    null,
    2,
  ) + "\n";
}

/** Generate README.md for the extension */
export function generateReadme(
  config: ServiceConfig,
  methods: { name: string; description: string; type: string }[],
): string {
  const scopeArg = config.scope === "org"
    ? "orgId=YOUR_ORG_ID"
    : config.scope === "group"
    ? "groupId=YOUR_GROUP_ID"
    : "";

  const globalArgLine = scopeArg
    ? `  --global-arg apiToken=SNYK_API_TOKEN \\
  --global-arg ${scopeArg}`
    : `  --global-arg apiToken=SNYK_API_TOKEN`;

  return `# @webframp/snyk/${config.name}

${config.description}

Auto-generated by \`scripts/snyk-codegen\` from the Snyk REST API OpenAPI specification.

## Usage

\`\`\`bash
swamp extension pull @webframp/snyk/${config.name}

swamp model create @webframp/snyk/${config.name} snyk-${config.name} \\
${globalArgLine}
\`\`\`

## Methods

| Method | Description |
|--------|-------------|
${methods.map((m) => `| \`${m.name}\` | ${m.description} |`).join("\n")}

## Development

\`\`\`bash
cd ${OUTPUT_BASE}/${config.name}
deno task check
deno task lint
deno task fmt
deno task test
\`\`\`

## License

Apache-2.0 — see [LICENSE.md](LICENSE.md).
`;
}

