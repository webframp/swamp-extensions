/**
 * Generate the shared _lib/api.ts helper for Snyk.
 *
 * Key differences from Cloudflare:
 * - JSON:API envelope (data/links, not success/result)
 * - Cursor pagination (follows links.next)
 * - Version query parameter on all requests
 * - Content-Type: application/vnd.api+json
 */
export function generateApiLib(): string {
  return `// Snyk API Helper
// Shared utilities for all Snyk models
// Auto-generated by scripts/snyk-codegen — do not edit manually.

const SNYK_API_BASE = "https://api.snyk.io/rest";

export interface SnykJsonApiResponse<T = unknown> {
  data: T;
  jsonapi: { version: string };
  links?: {
    self?: string;
    first?: string;
    next?: string | null;
    prev?: string | null;
  };
}

export interface SnykErrorResponse {
  jsonapi: { version: string };
  errors: Array<{
    status: string;
    detail: string;
    title?: string;
    code?: string;
  }>;
}

/**
 * Make a single Snyk API request.
 * Returns the flattened data (id + attributes merged).
 */
export async function snykApi(
  apiToken: string,
  method: string,
  path: string,
  version: string,
  body?: unknown,
): Promise<Record<string, unknown>> {
  const separator = path.includes("?") ? "&" : "?";
  const url = \`\${SNYK_API_BASE}\${path}\${separator}version=\${encodeURIComponent(version)}\`;

  const headers: Record<string, string> = {
    "Authorization": \`token \${apiToken}\`,
  };
  if (body !== undefined) {
    headers["Content-Type"] = "application/vnd.api+json";
  }

  const response = await fetch(url, {
    method,
    headers,
    body: body ? JSON.stringify(body) : undefined,
  });

  if (!response.ok) {
    const text = await response.text();
    let detail = text.slice(0, 500);
    try {
      const err = JSON.parse(text) as SnykErrorResponse;
      if (err.errors?.length) {
        detail = err.errors.map((e) => e.detail).join("; ");
      }
    } catch { /* use raw text */ }
    throw new Error(\`Snyk API HTTP \${response.status}: \${detail}\`);
  }

  // DELETE returns 204 No Content
  if (response.status === 204) {
    return {};
  }

  const json = await response.json() as SnykJsonApiResponse;
  return flattenData(json.data);
}

const MAX_PAGES = 20;

export interface PaginatedResult {
  results: Record<string, unknown>[];
  truncated: boolean;
  totalFetched: number;
}

/**
 * Paginate through a Snyk list endpoint using cursor-based pagination.
 * Follows links.next until exhausted or MAX_PAGES reached.
 */
export async function snykApiPaginated(
  apiToken: string,
  path: string,
  version: string,
  params?: Record<string, string>,
): Promise<PaginatedResult> {
  const allResults: Record<string, unknown>[] = [];
  let page = 0;
  let truncated = false;

  // Build initial URL with params
  const queryParts: string[] = [\`version=\${encodeURIComponent(version)}\`, "limit=100"];
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      if (v) queryParts.push(\`\${encodeURIComponent(k)}=\${encodeURIComponent(v)}\`);
    }
  }
  let nextUrl: string | null = \`\${SNYK_API_BASE}\${path}?\${queryParts.join("&")}\`;

  while (nextUrl && page < MAX_PAGES) {
    const response = await fetch(nextUrl, {
      headers: {
        "Authorization": \`token \${apiToken}\`,
      },
    });

    if (!response.ok) {
      const text = await response.text();
      let detail = text.slice(0, 500);
      try {
        const err = JSON.parse(text) as SnykErrorResponse;
        if (err.errors?.length) {
          detail = err.errors.map((e) => e.detail).join("; ");
        }
      } catch { /* use raw text */ }
      throw new Error(\`Snyk API HTTP \${response.status}: \${detail}\`);
    }

    const json = await response.json() as SnykJsonApiResponse<unknown[]>;
    const items = Array.isArray(json.data) ? json.data : json.data ? [json.data] : [];

    for (const item of items) {
      allResults.push(flattenData(item));
    }

    // Follow links.next for cursor pagination
    nextUrl = json.links?.next ?? null;
    if (nextUrl && !nextUrl.startsWith("http")) {
      // Relative URL — prepend base
      nextUrl = \`\${SNYK_API_BASE}\${nextUrl}\`;
    }

    page++;
  }

  if (page >= MAX_PAGES && nextUrl) {
    truncated = true;
  }

  return { results: allResults, truncated, totalFetched: allResults.length };
}

/**
 * Flatten a JSON:API data item: merge id + type + attributes into a flat object.
 * Relationships are stored as {rel_name}_id.
 */
function flattenData(data: unknown): Record<string, unknown> {
  if (!data || typeof data !== "object") return {};

  const item = data as Record<string, unknown>;
  const result: Record<string, unknown> = {};

  if (item.id) result.id = item.id;
  if (item.type) result.type = item.type;

  // Flatten attributes
  if (item.attributes && typeof item.attributes === "object") {
    const attrs = item.attributes as Record<string, unknown>;
    for (const [key, value] of Object.entries(attrs)) {
      result[key] = value;
    }
  }

  // Flatten relationship IDs
  if (item.relationships && typeof item.relationships === "object") {
    const rels = item.relationships as Record<string, unknown>;
    for (const [name, rel] of Object.entries(rels)) {
      if (rel && typeof rel === "object") {
        const relData = (rel as Record<string, unknown>).data;
        if (relData && typeof relData === "object") {
          result[\`\${name}_id\`] = (relData as Record<string, unknown>).id;
        }
      }
    }
  }

  return result;
}
`;
}

